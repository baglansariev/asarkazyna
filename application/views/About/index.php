					Что такое кооперативы???
				</p>
				<p class="text-content">
					Люди объединялись в группы с древнейших времён. Первобытные охотники сообща охотились, земледельцы обрабатывали поля. Что такое кооперативы они не знали. Но их объединения вполне можно отнести к современному понятию кооператива.

					Слово «кооператив» произошло от двух латинских корней co – "совместно", "сообща" и opus - "работа", "труд". Следовательно, отвечая на вопрос что такое кооперативы, определение общепринятое на международном уровне в упрощённом варианте переводится как совместное действие, сотрудничество.

					Кооператив – это объединение физических или юридических лиц для сотрудничества в различных сферах жизнедеятельности. Сюда относятся производство и сбыт продукции, строительство и эксплуатация зданий, закупка и потребление услуг и товаров. Добровольное объединение признаётся юридическим лицом, развивающимся за счёт самофинансирования и самоуправления.

					На основе долевого участия каждого члена кооператива создаётся кооперативное имущество.

					Перед объединением ставятся конкретные цели, создаётся общий фонд. Каждый член кооператива вносит в него пай (долю). Пайщики руководят кооперативом, отвечают за возможные риски, распределяют прибыль.

					Подобное объединение создаётся как некоммерческая организация для удовлетворения потребностей пайщиков.
				</p>
