					О кооперативе
				</p>
				<p class="text-content">
					Потребительский кооператив «Асар Казына» (далее - кооператив) создан в соответствии с решением учредительного собрания кооператива (протокол от «01» июня 2018 года) как добровольное объединение граждан на основе их членства в целях удовлетворения потребностей в приобретении недвижимого и движимого имущества путем объединения членами кооператива имущественных (паевых) взносов, который функционирует на основе добровольности вступления и выхода, обязательности уплаты имущественного (паевого) взноса, демократичности управления, взаимопомощи и защиты интересов его членов, а также доступности информации о деятельности кооператива для всех его членов.

					Предмет и цели деятельности кооператива
					Улучшение социального положения членов кооператива, путем объединения их вкладов, оказания взаимопомощи членами кооператива с целью приобретения недвижимого и движимого имущества, для дальнейшей передачи в собственность соответствующему члену кооператива в порядке соответствующей очередности, с отлагательным условием 100 (сто) процентного паенакопления в кооперативе.
